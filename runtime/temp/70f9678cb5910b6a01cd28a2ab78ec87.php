            <h3>发布了信息,为什么看不到？</h3>
        </div>
        <div class="content1">原因可能是：<br /> 　　(1)、按照信息类别不同，对您所发布的信息进行的归类整理，如：招商信息、代理信息等，您需要依据您发布信息时所选择的类别进行查找。如果您发布的是招商信息，在首页和招商信息库都可以看到；如果您发布的是代理信息，您可以通过本站的搜索引擎，输入搜索关键字，搜索查询到。
            <br /> 　　(2)、每时每刻都有用户在发布信息，因首页位置有限，可能与您基本同时发布的信息较多，已经把您发布的信息压到了后边。所以，您可以在招商信息库或代理信息库中搜索到。
            <br /> 　　(3)、对于匿名发布的所有信息，信息会归类入库，但在首页位置是不显示的，您需要通过本站首页的搜索引擎进行查询。
