            <h3>怎样发布信息才能获得更多的反馈？</h3>
        </div>
        <div class="content1">如果您想让自己的商业信息在众多同行中脱颖而出，引起潜在合作伙伴的关注，我们建议您：<br /> 　　(1)、每一条商业信息尽量针对具体产品详细描述，泛泛的企业介绍对买家吸引力较小；
            <br /> 　　(2)、填写的信息正文内容要尽量详实，对所经营的产品有比较充分的介绍，如产品规格、型号等；
            <br /> 　　(3)、选配产品样品图片，以增强效果，真实、形象地展现产品；
            <br /> 　　(4)、填写产品参数，专业详细地介绍产品品质及技术信息；
            <br /> 　　(5)、确保您的固定电话、传真、Email地址、手机号码填写正确，避免因写错联系方式而造成联系失败。
